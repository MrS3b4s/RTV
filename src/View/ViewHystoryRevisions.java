    private void btnFilterDateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFilterDateActionPerformed
       DefaultTableModel tb = new DefaultTableModel();
        tb.addColumn(" Vehicule DNI");
        tb.addColumn("Date");
        tb.addColumn("Technician");
        String[] d =new SimpleDateFormat("yyyy-MM-dd").format(jDateChooser1.getDate()).split("-");
        java.sql.Date dt = new java.sql.Date(Integer.parseInt(d[0])-1900, Integer.parseInt(d[1])-1, Integer.parseInt(d[2]));
        ArrayList<Revision> revisions = rc.revisionFilter(dt);
        for (int i = 0; i < revisions.size(); i++) {
            Object [] fil = new Object[3];
            fil[0] = revisions.get(i).getVehicule();
            fil[1] = revisions.get(i).getDate();
            fil[2] = revisions.get(i).getTechnician();
            tb.addRow(fil);
        }
        jTable1.setModel(tb);
    }//GEN-LAST:event_btnFilterDateActionPerformed

