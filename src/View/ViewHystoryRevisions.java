    public void updateTable(){
        DefaultTableModel tb = new DefaultTableModel();
        tb.addColumn(" Vehicule DNI");
        tb.addColumn("Date");
        tb.addColumn("Technician");
        ArrayList<Revision> revision = rc.revisionsList();
        for (int i = 0; i < revision.size(); i++) {
            Object [] fil = new Object[3];
            fil[0] = revision.get(i).getVehicule();
            fil[1] = revision.get(i).getDate();
            fil[2] = revision.get(i).getTechnician();
            tb.addRow(fil);
        }
        jTable1.setModel(tb);
    }
